void main() {
    print("\n");
    print("Looping Pertama");
    var nilai = 2;
    while (nilai <= 20) {
        print(nilai.toString() + " - I love coding");
        nilai+=2;
    }

    print("Looping Kedua");
    var nilai2 = 20;
    while (nilai2 >= 2) {
        print(nilai2.toString() + " - I will become a mobile developer");
        nilai2-=2;
    }
}